			<h1 class="hide-for-small">Sol y Luna</h1>
			<h2 class="show-for-small">Sol y Luna</h2>
			<hr>
			<p>
				Nuestra Bella Posada está ubicada en pleno corazón del parque Nacional Archipiélago de Los Roques muy cerca de la playa Cuenta con 4 habitaciones dobles y una triple todas con baño privado ventilador y aire acondicionado.
	
				Acogedores espacios comunes ventilados de estilo natural,  terraza con vista al mar donde podrán degustar exquisitas cenas que caracterizan la cocina de la posada servida  en un ambiente muy especial.
				
				Ofrecemos  Wi-Fi en las áreas comunes de la posada y cuenta con electricidad las 24 horas del día.
				
			</p>
			
			<p>
				El parque Nacional Archipiélago de Los Roques está ubicado en Venezuela, formado por un conjunto de islas y cayos,  tiene una superficie estimada en 40,61 km² al norte de la ciudad de Caracas en el Mar caribe y  representa uno de los principales atractivos turísticos del país por poseer arrecifes coralinos, Bancos de arenas blancas de origen coralino, y aguas cristalinas que se degradan desde el azul más intenso.
			</p>
				
			<p>
				El pueblo del Gran Roque es la principal localidad del archipiélago, se trata de un pequeño poblado que consta de calles de arena (las principales son calle Maráo, Calle Tortuga, Calle Cojinúa y Calle Chucho) hay un puesto de Inparques, una comisaría, un puesto de la Guardia Nacional de Venezuela, un pequeño estadio, escuela primaria y una iglesia católica, un Comando de Guardacosta de la Armada de Venezuela, la Superintendencia de Instituto Nacional de Parques, Bancos, farmacia, tiendas de  Souvenir, pequeños abastos, restaurantes. El aeropuerto está ubicado al borde del mar, a escasos metros de la playa, está separado del pueblo por una Laguna ubicada al este y norte del poblado. Un faro que no debe dejar de visitar parea contemplar la increíble puesta de sol.
			</p>
			
			<p>	
				Las principal atracción es sin lugar a dudas sus maravillosas playas donde se realizan actividades   buceo, pesca deportiva, windsurf, KiteSurf, Paddle, Kayak, que gustosamente la posada se las puede organizar. 
			</p>
